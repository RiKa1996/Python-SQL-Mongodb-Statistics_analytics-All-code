#Assignment Operators
x=10
print(x)

y=x
print(x)

z=10+2*y
print(z)

#Multiple Assignment
x,y=10,20
print(x,y)

a=10
b=10
c=10
a,b,c=10,10,10
print(a,b,c)

#chained Assignment
a=b=c=10
print(a,b,c)

#Compound Assignment(Arithmetic, Simple)
a=5
print(a)
a=a+3
print(a)

a+=3  #a=a+3
print(a)

a*=2  #a=a*2
print(a)
